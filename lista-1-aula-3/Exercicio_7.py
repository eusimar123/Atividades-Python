# 7. Escreva um script que receba um número e que ao final informe o dobro do número, o número ao quadrado e o número ao cubo.
numero = float (input(" Digite um número: "))
dobro = numero * 2
quadrado = numero ** 2
cubo = numero ** 3
print(" O dobro do número é:", dobro)
print(" O quadrado do número é:", quadrado)
print(" O cubo do número é", cubo)
